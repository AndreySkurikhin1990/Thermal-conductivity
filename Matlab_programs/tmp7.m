function tt = tmp71()
ep=0; lambda=0; Sp=0;  knu=0;
PP=6.6260755e-34;
PB=1.380658e-23;
c0=299792458;
c1=PP*c0^2
c2=PP*c0/PB
sig=2*c1*(pi^5)/(15*(c2^4))
n=Kramers_n();
tol=3e4; t0=273.15; T0=563+t0; TH=141+t0; Nt=3e3; 
Sp=dlvoVer53101; leSp=length(Sp); knu=SredGraf*1e6;
for  k=1:Nt+1 
    tem(k)=((T0^4+(TH^4-T0^4)*(k-1)/Nt)^0.25); end;
for k=1:leSp
Sp(k)=1e-2/Sp(k);
end
for p=1:Nt+1
T=tem(p);
ept=0; Ib=0;
    for k=1:leSp
lambda=Sp(k);
Ib(k)=c1/((lambda^5)*(exp(c2/(lambda*T))-1));
ept(k)=Ib(k)*knu(k);
    end
    otn(p)=trapz(Sp,Ib);
    ep(p)=trapz(Sp,ept)/otn(p);
    otn(p)=otn(p)/(sig*(T^4));
end
dl=1e-7:1e-7:5e-1; p=length(dl); T=T0; Ib=0;
for k=1:p
    lambda=dl(k);
    Ib(k)=c1/((lambda^5)*(exp(c2/(lambda*T))-1));
end
disp(2*pi*trapz(dl,Ib)/(sig*(T^4)));
q=plot(tem-t0,2*pi*otn,'-b');
set(q,'LineWidth',3);
xlabel('Температура, °С');
ylabel('Отношение');
title('График epsilon(t)');
hold on; grid on;
end

