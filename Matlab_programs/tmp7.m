function [ t ] = opredBoleeTochZnach(temp)
t1=2e1; cp1=4.183*1e3;
t2=3e1; cp2=4.174*1e3;
kn=(cp2-cp1)/(t2-t1);
n=length(temp); 
ktp=0;
for k=1:n
    ko=temp(k)-t1;
    ktp(k)=kn*ko+cp1;
end
t=ktp';
end
