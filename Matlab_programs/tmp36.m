function t = tmp36()
format long g;
ks=10;
te0=273.15;
te1=1000+te0; 
te2=(242+294)/2
te2=te2+te0;
y0=30e-3;
ko0=(te2-te1)/y0; 
ko1=te1;
h=12e-6
hk=0;
por84=0.5575;
hv=h*por84/(1-por84)
hk=0; xko=0; xna=0; tko=0;
for q=1:30
    x=0; tex=0;
x(1)=hk+h/2; 
ht=(h+hv);
tex(1)=polyval([ko0 ko1], x(1));
for k=2:ks
    x(k)=x(k-1)+ht;
    tex(k)=ko0*x(k)+ko1;
end
xko(q)=x(ks);
xna(q)=x(1);
tko(q)=tex(ks);
dex=x(ks)-x(1);
det=tex(ks)-tex(1);
dtx(q)=abs(det/dex);
tna(q)=tko(q)-dtx(q)*dex;
hk=hk+1e-3;
end
xko=xko';
xna=xna';
tko=tko';
dtx=dtx';
t=0;
end

function al = srealp(T)
te=1e2:1e-1:11e2;
te0=273.15;
te=te+te0;
f=1;
for k=1:length(te)
    if (te(k)>T)
        if (f>0)
        f=0;
        q=k;
        end
    end
end
alp=kopo();
al=alp(q-1)+(T-te(q-1))*(alp(q)-alp(q-1))/1e-1;
end
